package MouseOver;

public class FileXLRead {

	public static void main(String[] args) {
		int S = 50;
		String P = "50";

		System.out.println(S + S);
		System.out.println(P + P);

		int p = Integer.parseInt(P);// string to convert int
		System.out.println(p + p);// 20+20
		double n = Double.parseDouble(P);// int convert to double
		System.out.println(n + n);
	}

}
